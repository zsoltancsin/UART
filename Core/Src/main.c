// A megszakítás kezelő
void UART3_IRQHandler(void)
{
    uint8_t received_byte;

    if (__HAL_UART_GET_FLAG(&huart3, UART_FLAG_RXNE)) {
        // Egyetlen bájt beolvasása
        HAL_UART_Receive(&huart3, &received_byte, 1, HAL_MAX_DELAY);

        // Adat pufferbe helyezése
        static uint8_t index = 0;
        rx_buffer[index++] = received_byte;

        // Ha elértük a puffer méretét vagy új sor karakter érkezett, dolgozzuk fel
        if (index >= sizeof(rx_buffer) || received_byte == '\n') {
            pb_istream_t stream = pb_istream_from_buffer(rx_buffer, index);
            LedCommand led_command = LedCommand_init_zero;
            if (pb_decode(&stream, LedCommand_fields, &led_command)) {
                if (led_command.command == LedCommand_CommandType_LED_ON) {
                    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0, GPIO_PIN_SET);
                } else if (led_command.command == LedCommand_CommandType_LED_OFF) {
                    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0, GPIO_PIN_RESET);
                }
            }
            // Hibás adat esetén semmi nem történik, csak a puffer törlődik
            index = 0;  // Új adatfogadásra előkészítés
        }

        // Flag törlése
        __HAL_UART_CLEAR_FLAG(&huart3, UART_FLAG_RXNE);
    }
}

